for (const summary of exerciseSummaries) {
  if (!uniqueExerciseSummaryMap.has(summary.id)) {
    uniqueExerciseSummaryMap.set(summary.id, summary);
  }
}

const exerciseSummaryLookup = new Map<string, ExerciseSummary[]>();

for (const summary of uniqueExerciseSummaryMap.values()) {
  const existing = exerciseSummaryLookup.get(summary.category);
  if (existing) {
    existing.push(summary);
    continue;
  }

  exerciseSummaryLookup.set(summary.category, [summary]);
}

export function getExerciseSummariesByCategory(category: string): ExerciseSummary[] {
  return exerciseSummaryLookup.get(category) ?? [];
}
